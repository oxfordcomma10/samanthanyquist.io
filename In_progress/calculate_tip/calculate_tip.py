def calculate_eighteen (total_amount):
    total_amount = float(total_amount)
    eighteen = total_amount * 0.18
    return eighteen

def calculate_eighteen_total(total_amount):
    total_amount = float(total_amount)
    return calculate_eighteen(total_amount) + total_amount

def calculate_twenty(total_amount):
    total_amount = float(total_amount)
    twenty = total_amount * 0.2
    return twenty

